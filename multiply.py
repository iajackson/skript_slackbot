"""
Multiply Module

Multiplies two numbers together

Functions:
    1. run(a,b): Multiplies two numbers together

Author: Ian Jackson
"""


def run(a, b):
    """
    Multiplies two numbers together

    Parameters:
        a (string): The first number
        b (string): The second number

    Returns:
        int: The product of a and b

    """
    return int(a) * int(b)
